Point2Di operator + (Point2Di &p1, Point2Di &p2)
{
	Point2Di t;
	t.x = p1.x + p2.x;
	t.y = p1.y + p2.y;
	return t;
}

//////////////////////////////////////////////////////////////////////

Point2Di operator - (Point2Di &p1, Point2Di &p2)
{
	Point2Di t;
	t.x = p1.x - p2.x;
	t.y = p1.y - p2.y;
	return t;
}

//////////////////////////////////////////////////////////////////////

float operator * (Point2Di &p1, Point2Di &p2)
{
	return (float)p1.x*p2.x + (float)p1.y*p2.y;
}

